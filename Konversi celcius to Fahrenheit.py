# Program python untuk konversi Celcius ke Fahrenheit

# ubah nilai untuk hasil yang berbeda
celcius = float(input("Masukkan Suhu Celcius = "))

# menghitung fahrenheit
fahrenheit = celcius * 1.8 + 32
    
#menampilkan hasil perhitungan
print(f"""
Konversi Suhu Celcius menjadi Suhu Fahrenheit
= ({celcius}°C * 1.8) + 32
= {fahrenheit}°f
""")
